set -e

## create the operator file using all the yamls

echo "# This manifest is autogenerated via 'make manifests' command.
# Do the modification to the yamls in deploy/yamls/  directory
# and then run 'make manifests' command

# This manifest deploys the OpenEBS NDM components with associated RBAC rules.
" > deploy/ndm-operator.yaml

# create the ndm-operator.yaml manifest
{
  # Add namespace creation to the Operator yaml
  cat deploy/yamls/namespace.yaml

  # Add NDM config map
  cat deploy/yamls/node-disk-manager-config.yaml

  # Add the openebs service account
  cat deploy/yamls/serviceaccount.yaml

  # Add clusterrole and rolebinding
  cat deploy/yamls/clusterrole.yaml
  cat deploy/yamls/clusterrolebinding.yaml

  # Add the basic components: NDM daemon and operator
  cat deploy/yamls/node-disk-manager.yaml
  cat deploy/yamls/node-disk-operator.yaml

  # Add optional components for metrics: NDM cluster and node exporter
  cat deploy/yamls/ndm-cluster-exporter.yaml
  cat deploy/yamls/ndm-node-exporter.yaml
} >> deploy/ndm-operator.yaml